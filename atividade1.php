
1- Termine o script abaixo, somando o valor das variáveis valor1 e valor2, e multiplicando o
resultado pelo valor de valor1 exibindo o resultado na tela 
<br>
<br>valor1 = 2; 
<br>valor2 = 7;
<br>
<?php
$valor1 = 2;
$valor2 = 7; 
// Somando o valor das variáveis $valor1 e $valor2
$soma = $valor1 + $valor2;
    echo "<br> Soma do Valor1 + valor2: " . $soma;
// Multiplicando o resultado pelo valor de $valor1
$resultado = $soma * $valor1;
// Exibindo o resultado na tela
echo "<br> O resultado da operação é: " . $resultado;
?>
